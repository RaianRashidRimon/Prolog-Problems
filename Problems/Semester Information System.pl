% Queries

% Get all courses for a semester
display_courses(Semester) :-
    course(Semester, Code, Title, Teacher),
    format('~w - ~w (Assigned Teacher: ~w)~n', [Code, Title, Teacher]), fail.
display_courses(_).

% Find instructor for a course
find_instructor(CourseCode) :-
    course(_, CourseCode, Title, Teacher),
    format('Course: ~w (~w) is taught by ~w.~n', [CourseCode, Title, Teacher]).

% Find all courses taught by a specific teacher
find_courses_by_teacher(Teacher) :-
    course(_, Code, Title, Teacher),
    format('~w - ~w~n', [Code, Title]), fail.
find_courses_by_teacher(_).

% Find course code by course title
find_course_code(Title) :-
    course(_, Code, Title, _),
    format('Course code of ~w is ~w~n', [Title, Code]).

% Find course title by course code
find_course_title(Code) :-
    course(_, Code, Title, _),
    format('Course Code: ~w corresponds to Title: ~w~n', [Code, Title]).

% Find course details by course code
find_course_details(Code) :-
    course(Semester, Code, Title, Teacher),
    format('Course Code: ~w (~w) is taught in ~w by ~w.~n', [Code, Title, Semester, Teacher]).

% Find all LAB courses in a specific semester
find_lab_courses(Semester) :-
    course(Semester, Code, Title, Teacher),
    sub_atom(Title, _, _, _, 'LAB'),
    format('~w - ~w (Assigned Teacher: ~w)~n', [Code, Title, Teacher]), fail.
find_lab_courses(_).

